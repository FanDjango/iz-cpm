    pub fn decode(&self, state: &mut State) -> &Option<Opcode> {
        let b0 = state.advance_pc();
        match b0 {
            0xcb => &self.prefix_cb[state.advance_pc() as usize],
            0xed => &self.prefix_ed[state.advance_pc() as usize],
            0xdd => {
                let b1 = state.advance_pc();
                if b1 == 0xcb {
                    &self.prefix_ddcb[state.advance_pc() as usize]
                } else {
                    &self.prefix_dd[b1 as usize]
                }
            },
            0xfd => {
                let b1 = state.advance_pc();
                if b1 == 0xcb {
                    &self.prefix_fdcb[state.advance_pc() as usize]
                } else {
                    &self.prefix_fd[b1 as usize]
                }
            },
            _ => &self.no_prefix[b0 as usize]
            // TODO: verify how dddd, dded, ddfd, fddd, fded and fdfd work
        }
    }

    fn load_no_prefix(&mut self) {
        for c in 0..=255 {
            //let opcode: Option<Opcode>;
            let p = DecodingHelper::parts(c);
            let opcode = match p.x {
                0 => match p.z {
                    0 => match p.y { // Relative jumps and assorted ops.
                        0 => Some(build_nop()), // NOP
                        1 => None,
                        2 => None,
                        3 => None,
                        4..=7 => None,
                        _ => panic!("Unreachable")
                    },
                    1 => match p.q {
                        0 =>  Some(build_ld_rr_nn(p.p)), // LD rp[p], nn -- 16-bit load add
                        1 =>  Some(build_add_hl_rr(p.p)), // ADD HL, rp[p] -- 16-bit add
                        _ => panic!("Unreachable")
                    },
                    2 => None,
                    3 => match p.q {
                        0 =>  Some(build_inc_dec_rr(p.p, true)), // INC rp[p] -- 16-bit inc
                        1 =>  Some(build_inc_dec_rr(p.p, false)), // DEC rp[p] -- 16-bit dec
                        _ => panic!("Unreachable")                       
                    },
                    4 => match p.y {
                        6 => None, // INC (HL) -- 8 bit inc
                        0..=7 => Some(build_inc_r(p.y)), // INC r[y] -- 8 bit inc
                        _ => panic!("Unreachable")
                    },
                    5 => match p.y {
                        6 => None, // DEC (HL) -- 8 bit dec
                        0..=7 => Some(build_dec_r(p.y)), // DEC r[y] -- 8 bit dec
                        _ => panic!("Unreachable")
                    },
                    6 => match p.y {
                        6 => None, // LD (HL), n -- 8 bit load imm
                        0..=7 => Some(build_ld_r_n(p.y)), // LD r[y], n -- 8 bit load imm
                        _ => panic!("Unreachable")
                    },
                    7 => None,
                    _ => panic!("Unreachable")
                },
                1 => None,
                2 => None,
                3 => None,
                4 => None,
                5 => None,
                6 => None,
                7 => None,
                _ => panic!("Unreachable")
            };

            match opcode.as_ref() {
                None => (),
                Some(o) => println!("0x{:02x} {:20}: {:?}", c, o.name, p)
            }
            self.no_prefix[c as usize] = opcode;
        }
    }
